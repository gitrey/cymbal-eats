package org.google.demo;

/**
 * Status enum
 * This enum represents the status of a menu item.
 */
public enum Status {
    /**
     * Processing
     * The menu item is being processed.
     */
    Processing,
    /**
     * Ready
     * The menu item is ready.
     */
    Ready,
    /**
     * Failed
     * The menu item has failed.
     */
    Failed
}